public class string 
{
	public static void main(String args[])
	{
		String gt = "Hello";

		System.out.println(gt.length());
		System.out.println(gt.charAt(0));
		System.out.println(gt.indexOf("llo"));
		System.out.println(gt.substring(2));
		System.out.println(gt.substring(1, 3));

	}
}